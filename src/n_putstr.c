#include "ft_printf.h"

void	n_putstr(char const *s, t_valeur *v)
{
	int i;

	if (!s)
		return ;
	i = 0;
	while (s[i] && s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
		v->rest++;
	}
}
